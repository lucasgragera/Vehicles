public class Fiat {
    private String model;
    private int year;
    // Constructor with parameters
    public Fiat(String model, int year) {
        this.model = model;
        this.year = year;
    }

    // Getter for model
    public String getModel() {
        return model;
    }

    // Setter for model
    public void setModel(String model) {
        this.model = model;
    }

    // Getter for model
    public int getYear() {
        return year;
    }

    // Setter for model
    public void setYear(int year) {
        this.year = year;
    }
}
