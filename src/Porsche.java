public class Porsche {
    private String model;
    private int year;
    // Constructor with parameters
    public Porsche(String model, int year) {
        this.model = model;
        this.year = year;
    }

    // Getter for name
    public String getModel() {
        return model;
    }

    // Setter for name
    public void setModel(String model) {
        this.model = model;
    }

    // Getter for age
    public int getYear() {
        return year;
    }

    // Setter for age
    public void setYear(int year) {
        this.year = year;
    }
}
